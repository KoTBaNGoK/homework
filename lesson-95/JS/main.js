
// let - создает переменную, которую можно в последствии поменять

let firstName = 'Kamil'

// const - задает неизменяемую переменную

const lastName = 'Yusupov'

// var - старый способ присвоения переменных, схожий с let имеющий свои особености

var age = '18 years old'

const hobby = 'painting'

const post = 'programmer'

console.log(firstName, lastName)
console.log(age)
console.log(hobby)
console.log(post)
